$(() => {
  // **************************************************************************
  // * Global Variables
  // **************************************************************************

  const $openBtn = $('#openModal');
  const $modal = $('#modal');
  const $closeBtn = $('#close');

  // **************************************************************************
  // * Event Handlers
  // **************************************************************************

  // *************************
  // * For Instruction Modal
  // *************************
  const openModal = () => {
    $modal.css('display', 'block');
  }
  const closeModal = () => {
    $modal.css('display', 'none');
  }

  // **************************************************************************
  // * Event Listeners
  // **************************************************************************

  // ************************
  // * For Instruction Modal
  // ************************
  $("button").on("click", openModal);
  $closeBtn.on('click', closeModal);

  // *****************
  // * For the Popper
  // *****************
  $(".popper").on("click", () => {
    console.log("clicked!");
  });





}); // Closes window.onload
